#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	int		str_len;
	size_t	i;

	str_len = ft_strlen(src);
	i = 0;
	if (size == 0)
		return (str_len);
	while (src[i] && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (str_len);
}
